(
    id        int         NOT NULL AUTO_INCREMENT,
    nev       varchar(64) NOT NULL UNIQUE,
    ar        int         NOT NULL,
    etelkatId int         NOT NULL,
    primary key (id)
);

-- Rendelések
CREATE TABLE rendeles
(
    id            int  NOT NULL AUTO_INCREMENT,
    felhasznaloId int  NOT NULL,
    idopont       time NOT NULL,
    primary key (id)
);

-- Rendeléselemek
